// Fisher-Yates shuffle algorithm
function shuffleArray<T>(array: T[]): T[] {
  const arr = [...array];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

// Function to dynamically build worksheets with varied, randomized, non-overlapping questions
export function generateDynamicWorksheets(
  topicType: 'all' | 'unit' | 'lesson',
  unitId: string,
  lessonId: string,
  pageCount: number
): any[] {
  // 1. Compile target lessons
  let targetLessonIds: string[] = [];
  
  if (topicType === 'lesson') {
    targetLessonIds = [lessonId];
  } else if (topicType === 'unit') {
    const unit = UNITS_DATA.find(u => u.id === unitId);
    if (unit) {
      targetLessonIds = unit.lessons.map(l => l.id);
    } else {
      targetLessonIds = ['u1-l1'];
    }
  } else {
    // 'all'
    targetLessonIds = UNITS_DATA.flatMap(u => u.lessons.map(l => l.id));
  }

  if (targetLessonIds.length === 0) {
    targetLessonIds = ['u1-l1'];
  }

  // 2. Accumulate ALL available questions across all target lessons
  let allMC: PoolMCQuestion[] = [];
  let allFB: PoolFillInBlank[] = [];
  let allMatching: PoolMatchingPair[] = [];
  let allOdd: PoolOddOneOut[] = [];
  let allDefs: PoolDefinition[] = [];
  const availableDiagrams: PoolDiagramQuestion[] = [];

  for (const lid of targetLessonIds) {
    const pool = WORKSHEET_QUESTIONS_POOL[lid];
    if (pool) {
      allMC = [...allMC, ...pool.multipleChoice];
      allFB = [...allFB, ...pool.fillInBlanks];
      allMatching = [...allMatching, ...pool.matching];
      allOdd = [...allOdd, ...pool.oddOneOut];
      allDefs = [...allDefs, ...pool.definitions];
      if (pool.diagram) {
        availableDiagrams.push(pool.diagram);
      }
    }
  }

  // Secure fallback items to guarantee no crash if list is empty
  if (allMC.length === 0) allMC = [...WORKSHEET_QUESTIONS_POOL['u1-l1'].multipleChoice];
  if (allFB.length === 0) allFB = [...WORKSHEET_QUESTIONS_POOL['u1-l1'].fillInBlanks];
  if (allMatching.length === 0) allMatching = [...WORKSHEET_QUESTIONS_POOL['u1-l1'].matching];
  if (allOdd.length === 0) allOdd = [...WORKSHEET_QUESTIONS_POOL['u1-l1'].oddOneOut];
  if (allDefs.length === 0) allDefs = [...WORKSHEET_QUESTIONS_POOL['u1-l1'].definitions];

  // 3. Shuffle global arrays so we get completely randomized variation on every generation!
  const shuffledMC = shuffleArray(allMC);
  const shuffledFB = shuffleArray(allFB);
  const shuffledMatching = shuffleArray(allMatching);
  const shuffledOdd = shuffleArray(allOdd);
  const shuffledDefs = shuffleArray(allDefs);

  const generatedPages: any[] = [];

  for (let i = 0; i < pageCount; i++) {
    let pageTitle = "ورقة عمل تكنولوجية";
    let topicName = "تمارين عامة ومراجعة شاملة";
    let unitTitle = "منهج تكنولوجيا المعلومات والاتصالات";

    if (topicType === 'lesson') {
      const mainLessonId = targetLessonIds[0];
      for (const u of UNITS_DATA) {
        const les = u.lessons.find(l => l.id === mainLessonId);
        if (les) {
          pageTitle = les.title;
          topicName = les.objectives[0] || "تطبيق عملي وأوراق مراجعة ذكية";
          const uNum = u.number === 1 ? 'الأولى' : u.number === 2 ? 'الثانية' : u.number === 3 ? 'الثالثة' : u.number === 4 ? 'الرابعة' : 'الخامسة';
          unitTitle = `الوحدة ${uNum}: ${u.title}`;
          break;
        }
      }
      if (pageCount > 1) {
        pageTitle += ` - المجموعة (${i + 1})`;
      }
    } else if (topicType === 'unit') {
      const unit = UNITS_DATA.find(u => u.id === unitId);
      if (unit) {
        const uNum = unit.number === 1 ? 'الأولى' : unit.number === 2 ? 'الثانية' : unit.number === 3 ? 'الثالثة' : unit.number === 4 ? 'الرابعة' : 'الخامسة';
        unitTitle = `الوحدة ${uNum}: ${unit.title}`;
        pageTitle = `اختبار مراجعة الوحدة الشامل`;
        topicName = `سلسلة قياس المهارات التقنية وتقييم الكفاءات`;
      }
      if (pageCount > 1) {
        pageTitle += ` - الصفحة (${i + 1})`;
      }
    } else {
      // 'all'
      unitTitle = "مقرر تكنولوجيا المعلومات والاتصالات - الصف السادس";
      pageTitle = "الامتحان التجريبي النهائي الموحد";
      topicName = "المراجعة الختامية لتقييم مخرجات التعلم السنوية";
      if (pageCount > 1) {
        pageTitle += ` - نموذج ${i + 1}`;
      }
    }

    // Pick 3 multiple choice questions based on current page index sequentially to prevent inner overlaps
    const selectedMCList = [];
    for (let j = 0; j < 3; j++) {
      const idx = (i * 3 + j) % shuffledMC.length;
      selectedMCList.push(shuffledMC[idx]);
    }

    // Pick 3 fill in blanks
    const selectedFBList = [];
    for (let j = 0; j < 3; j++) {
      const idx = (i * 3 + j) % shuffledFB.length;
      selectedFBList.push(shuffledFB[idx]);
    }

    // Pick 4 matching pairs
    const selectedMatchingList: PoolMatchingPair[] = [];
    for (let j = 0; j < 4; j++) {
      const idx = (i * 4 + j) % shuffledMatching.length;
      selectedMatchingList.push(shuffledMatching[idx]);
    }

    // Pick 1 odd-one-out question
    const selectedOddItem = shuffledOdd[i % shuffledOdd.length];

    // Pick 2 definitions
    const selectedDefsList = [];
    for (let j = 0; j < 2; j++) {
      const idx = (i * 2 + j) % shuffledDefs.length;
      selectedDefsList.push(shuffledDefs[idx]);
    }

    // Format and shuffle matching columns (Col B shuffled, map retains correctMatch value)
    const colAItems = selectedMatchingList.map(item => item.term);
    const originalDefs = selectedMatchingList.map(item => item.definition);
    const shuffledDefsForB = shuffleArray(originalDefs);

    const processedMatching = colAItems.map((term, index) => {
      const originalItem = selectedMatchingList.find(item => item.term === term)!;
      return {
        colA: term,
        colB: shuffledDefsForB[index],
        correctMatch: originalItem.definition
      };
    });

    let selectedDiagram: PoolDiagramQuestion | undefined = undefined;
    if (availableDiagrams.length > 0) {
      selectedDiagram = availableDiagrams[i % availableDiagrams.length];
    }

    const singlePage = {
      pageNumber: i + 1,
      title: pageTitle,
      topicName: topicName,
      unitTitle: unitTitle,
      multipleChoice: selectedMCList,
      fillInBlanks: selectedFBList,
      matching: processedMatching,
      oddOneOut: {
        words: selectedOddItem.words,
        oddWord: selectedOddItem.oddWord,
        reason: selectedOddItem.reason
      },
      definitions: selectedDefsList,
      diagram: selectedDiagram
    };

    generatedPages.push(singlePage);
  }

  return generatedPages;
}
